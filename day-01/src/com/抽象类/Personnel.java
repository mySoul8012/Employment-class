package com.抽象类;

// 人员类
public abstract  class  Personnel {
    private String sex;
    private int age;
    private String name;

    public abstract void eat();

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
